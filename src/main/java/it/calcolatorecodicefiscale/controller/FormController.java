	private Sesso controllaSesso() {
		if (uomo.isSelected())
			return Sesso.UOMO;
		else
			return Sesso.DONNA;
	}

	@FXML
	public void statoEsteroAction(ActionEvent event) {
		if (statoEstero.isSelected()) {
			statoEsteroNascita.setDisable(false);
			luogoNascita.setDisable(true);
		}
		else
			statoEsteroNascita.setDisable(true);
		    luogoNascita.setDisable(false);
	}

	@FXML
	public void calcolaAction(ActionEvent event) {
		persona = new Persona(nome.getText().toUpperCase(), cognome.getText().toUpperCase(), dataNascita.getValue(),
				luogoNascita.getValue(), statoEsteroNascita.getValue(), controllaSesso());

		CodiceFiscale codiceFiscale = new CodiceFiscale(codiceFiscaleService.calcolaCodiceFiscale(persona));
		persona.setCodiceFiscale(codiceFiscale);
		codice.setText(persona.getCodiceFiscale().getCodiceFiscale());
	}

	@FXML
	public void cancellaAction(ActionEvent event) {
		nome.setText(null);
		cognome.setText(null);
		dataNascita.setValue(null);
		luogoNascita.setValue(null);
	}

	@FXML
	public void copiaAction(ActionEvent event) {
		StringSelection selection = new StringSelection(codice.getText());
		Toolkit.getDefaultToolkit().getSystemClipboard().setContents(selection, null);
		nome.setText(null);
		cognome.setText(null);
		dataNascita.setValue(null);
		luogoNascita.setValue(null);
	}

